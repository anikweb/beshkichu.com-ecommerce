     */
    public function update(Request $request, PrivacyPolicy $privacyPolicy)
    {
        if(auth()->user()->can('site settings edit')){
            $privacyPolicy->description = $request->description;
            $privacyPolicy->save();
            return redirect()->route('privacy-policy.index')->with('success','Privacy policy updated!');
        }else{
            return abort('404');
        }
