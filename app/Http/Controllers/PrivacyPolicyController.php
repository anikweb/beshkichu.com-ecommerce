     */
    public function edit(PrivacyPolicy $privacyPolicy)
    {
        if(auth()->user()->can('site settings edit')){
            return view('backend.pages.privacy_policy.edit',compact('privacyPolicy'));
        }else{
            return abort(404);
        }
